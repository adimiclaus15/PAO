package com.company;

public class Student {
    private String nume;
    private int nota;
    public Student(){}
    public void setNume(String nume)
    {
        this.nume=nume;
    }
    public void setNota(int nota)
    {
        this.nota=nota;
    }
    public String getNume()
    {
        return this.nume;
    }
    public int getNota()
    {
        return this.nota;
    }
}
